     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = new Partie();
        $data->pseudo = $request->input('pseudo');
        $data->score = $request->input('score');
        if ($data->save()) {
            return new PartieResource($data);
        };
